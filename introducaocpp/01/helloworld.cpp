// Programa de impressão de texto. Livro C++ Como Programar

#include <iostream> // permite que o programa gere saída de dados na tela

using namespace std; // comando que inicia estrutura c++

// a função main inicia a execução do programa
int main()
{
    /* Os parênteses depois de main indicam que main é um bloco de construção de programa, ou seja uma função*/
    std::cout <<"Hello World!\n\n"; // exibe a mensagem

    return 0; // indica que o programa terminou com sucesso
} // fim da função main